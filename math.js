//Math.round Metodu
var x = Math.round(2.5);
console.log(x);

var x = Math.round(2.4);
console.log(x);

//Math.ceil Metodu
var x = Math.ceil(2.5);
console.log(x);

var x = Math.ceil(2.4);
console.log(x);


//Math.floor Metodu
var x = Math.floor(2.5);
console.log(x);

var x = Math.floor(2.4);
console.log(x);

//Math.abs Metodu
var x = Math.abs(-2.4);
console.log(x);


//Math.min Metodu
var x = Math.min(-5,10,-50,70);
console.log(x);


//Math.max Metodu
var x = Math.max(-5,10,-50,70);
